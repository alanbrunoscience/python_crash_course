def greet_user(username):
    """Display a simple greeting."""
    print(f"Hello, {username.title()}!")

greet_user('jesse')

# Explanation of the main details

# - 'def' -> Function definition;
# - """Display a simple greeting.""" -> Docstring - describes what the function 
# does;
# - 'username' -> is a parameter. A piece of information that the function needs
# to do its job;
# - greet_user('jesse') -> 'jesse' is an example of an argument. An argument is 
# a piece of information that's passed from a function call to a function.
